//Улитка
//Улитка ползет по вертикальному шесту высотой H метров, поднимаясь за день на A метров, а за ночь спускаясь на B метров. На какой день улитка доползет до вершины шеста?

#include <iostream>
using namespace std;
int main() {
	int h, a, b;
	h > b;
	a > b;
	h, a, b < 100;
	cin >> h >> a >> b;
	cout << 1 + (h - b - 1) / (a - b);
	return 0;
} 